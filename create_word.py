from docx import Document
from docx.shared import Pt, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH

# 创建文档
doc = Document()

# 设置标题
title = doc.add_heading('母爱如山——论母爱的伟大与无私', 0)
title.alignment = WD_ALIGN_PARAGRAPH.CENTER

# 引言
doc.add_heading('引言', level=1)
doc.add_paragraph(
    '"世界上的一切光荣和骄傲，都来自母亲。"这是苏联作家高尔基对母亲最真挚的赞美。'
    '母亲，这个平凡而伟大的称谓，承载着人类最深沉的情感。母爱如涓涓细流，滋润着我们的心田；'
    '母爱如巍峨高山，为我们遮风挡雨。本文将探讨母爱的特质、表现形式及其对我们成长的深远影响。'
)

# 第一部分
doc.add_heading('一、母爱的无私与奉献', level=1)
doc.add_paragraph(
    '母爱的第一个显著特征是无私。母亲对子女的爱，不求回报，不计得失。正如唐代诗人孟郊在《游子吟》中所写：'
    '"谁言寸草心，报得三春晖。"子女那微小的心意，怎能报答母亲如春日阳光般温暖的恩情？'
)
doc.add_paragraph(
    '母亲从怀孕那一刻起，便开始了一场漫长的奉献之旅。十月怀胎的艰辛，分娩时的痛苦，'
    '养育过程中的操劳，无不体现着母爱的伟大。中国著名作家冰心曾说："母亲啊！你是荷叶，我是红莲。'
    '心中的雨点来了，除了你，谁是我在无遮拦天空下的荫蔽？"这句话生动地描绘了母亲作为保护者的角色。'
)

# 第二部分
doc.add_heading('二、母爱的细腻与包容', level=1)
doc.add_paragraph(
    '母爱的第二个特征是细腻。母亲往往能敏锐地察觉子女的情绪变化，给予及时的关怀。'
    '无论是生病时的悉心照料，还是挫折时的温柔安慰，母亲总是用最细腻的方式表达着爱。'
)
doc.add_paragraph(
    '同时，母爱具有无限的包容性。无论子女犯下什么错误，母亲总是第一个原谅的人。'
    '法国作家罗曼·罗兰说过："母爱是一种巨大的火焰。"这火焰不仅温暖，更能照亮子女前行的道路，'
    '包容他们的一切过失。'
)

# 第三部分
doc.add_heading('三、母爱对人格塑造的影响', level=1)
doc.add_paragraph(
    '母爱对子女的人格塑造有着深远的影响。心理学家弗洛伊德认为，早期母婴关系对个体心理发展具有决定性作用。'
    '一个得到充分母爱的孩子，往往更加自信、乐观，具有更强的安全感。'
)
doc.add_paragraph(
    '中国著名教育家陶行知先生曾说："教育不能创造什么，但它能启发儿童创造力以从事于创造工作。"'
    '而母亲正是孩子最早的教育者，她们用爱启发着孩子的潜能，塑造着孩子的品格。'
)

# 第四部分
doc.add_heading('四、现代社会中的母爱', level=1)
doc.add_paragraph(
    '在现代社会，母爱的表现形式更加多元化。职业母亲需要平衡工作与家庭，单亲母亲需要独自承担养育责任，'
    '但无论形式如何变化，母爱的本质始终未变。'
)
doc.add_paragraph(
    '然而，我们也应该看到，过度溺爱并非真正的母爱。适当的放手与引导，让子女学会独立，'
    '才是现代母爱的应有之义。正如龙应台在《目送》中写道："所谓父女母子一场，只不过意味着，'
    '你和他的缘分就是今生今世不断地在目送他的背影渐行渐远。"'
)

# 结语
doc.add_heading('结语', level=1)
doc.add_paragraph(
    '"慈母手中线，游子身上衣。"母亲的爱，如同那细密的针脚，编织着我们生命的底色。'
    '在这个快节奏的时代，我们更应该珍惜与母亲相处的时光，感恩那份无声却深沉的爱。'
)
doc.add_paragraph(
    '让我们铭记英国诗人乔治·麦克唐纳的话："母亲的爱是永恒的，它超越时间、空间和生死的界限。"'
    '愿我们都能用实际行动回报母爱，让这份人间最珍贵的情感得以传承与延续。'
)

